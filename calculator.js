// Define the numbers and the operation here
const two= 2;
const four= 4;
const calculator= "+"


// Write if-statements here
if (calculator== "+") {
    console.log (two+four);
}
else if (calculator=== "*") {
    console.log (two*four);
}
else if (calculator=== "-") {
    console.log (two-four);
}
else if (calculator=== "/") {
    console.log (two/four);
}
else {
    console.log("Nothing ^-^");
}