const two= 2;
const four= 4;
const calculator= "+"

if (calculator== "+") {
    console.log (two+four);
}
else if (calculator=== "*") {
    console.log (two*four);
}
else if (calculator=== "-") {
    console.log (two-four);
}
else if (calculator=== "/") {
    console.log (two/four);
}
else {
    console.log("Nothing ^-^");
}